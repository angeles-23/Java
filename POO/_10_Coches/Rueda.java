public class Rueda {
    
    private int radio;
    private int kilometros;

    
    
    public Rueda(int radio, int kilometros) {
        this.radio = radio;
        this.kilometros = kilometros;
    }

    public int getRadio() {
        return radio;
    }

    
    public int getKilometros() {
        return kilometros;
    }

    public void setRadio(int radio) {
        this.radio = radio;
    }

    public void setKilometros(int kilometros) {
        this.kilometros = kilometros;
    }
    
    
}//
